#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	c;

	c = 0;
	while (s[c] != '\0')
	{
		if (write(fd, &s[c], 1) == -1)
			return (-1);
		c++;
	}
	return (0);
}
